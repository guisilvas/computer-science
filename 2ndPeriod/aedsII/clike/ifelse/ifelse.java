package aedsII.clike.ifelse;

public class ifelse {
    public static void main(String[] args) {
        int x=3;

        if(x%2==0)
        {
            System.out.println("The number " + x + " is even");
        }
        else
        {
            System.out.println("The number " + x + " is odd");
        }
    }
}
